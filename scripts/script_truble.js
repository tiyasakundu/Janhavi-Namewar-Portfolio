// Get the elements for Truble 5 overlay
const overlayBtn = document.getElementById('trubleOverlayBtn');
const overlayContainer = document.getElementById('overlayContainer');
const closeOverlay = document.getElementById('closeOverlay');

// Show the overlay when the button for Truble 5 is clicked
overlayBtn.addEventListener('click', function() {
    overlayContainer.classList.add('active');
});

// Close the overlay for Truble 5 when the close button is clicked
closeOverlay.addEventListener('click', function() {
    overlayContainer.classList.remove('active');
});

// Optional: Close the overlay when clicking outside the image
overlayContainer.addEventListener('click', function(e) {
    if (e.target === overlayContainer) {
        overlayContainer.classList.remove('active');
    }
});

